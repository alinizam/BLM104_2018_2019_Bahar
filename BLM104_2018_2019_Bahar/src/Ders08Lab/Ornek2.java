    private void cbUnvanItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_cbUnvanItemStateChanged
        // TODO add your handling code here:
        if (cbUnvan.getSelectedIndex()==-1){return;}
        DefaultTableModel dtm=new DefaultTableModel();
        dtm.setColumnIdentifiers(new String[]{"Id","Adi","Soyadi","Maa≈ü"});
        try {
            // TODO add your handling code here:
            Statement s=con.createStatement();
            int unvanId=((Unvan)cbUnvan.getSelectedItem()).unvanId;
            String sql="SELECT * FROM personel WHERE unvan_id="+unvanId;
            ResultSet rs=s.executeQuery(sql);
            DefaultComboBoxModel dcm=new DefaultComboBoxModel();
            while (rs.next()){
                dtm.addRow(new Object[]{rs.getInt("personel_id"),
                                        rs.getString("adi"),
                                        rs.getString("soyadi"),
                                        rs.getInt("maas")
                });             
            }
            jTable1.setModel(dtm);
            
        } catch (SQLException ex) {
            Logger.getLogger(Ornek1.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_cbUnvanItemStateChanged

